package main

import (
    "bufio"
    "fmt"
    "log"
    "net"
    "os"
    "strings"
    "sync"
    "time"
)

type Client struct {
    Conn net.Conn
    Name string
}

var (
    clients     = make(map[net.Conn]Client)
    mutex       sync.Mutex
    messages    []string
    maxClients  = 10
)

func main() {
    port := "8989"
    if len(os.Args) > 1 {
        port = os.Args[1]
    }

    listener, err := net.Listen("tcp", ":"+port)
    if err != nil {
        log.Fatalf("Erreur lors de l'écoute du port %s: %v\n", port, err)
    }
    defer listener.Close()

    fmt.Println("Serveur démarré, écoute sur le port :", port)

    for {
        if len(clients) < maxClients {
            conn, err := listener.Accept()
            if err != nil {
                fmt.Println("Erreur lors de l'acceptation :", err)
                continue
            }
            go handleClient(conn)
        }
    }
}

func handleClient(conn net.Conn) {
    defer conn.Close()

    conn.Write([]byte(banner()))
    conn.Write([]byte("[ENTER YOUR NAME]: "))
    
    name, _ := bufio.NewReader(conn).ReadString('\n')
    name = strings.TrimSpace(name)

    if name == "" {
        conn.Write([]byte("[ERROR] Empty name not allowed.\n"))
        return
    }

    mutex.Lock()
    clients[conn] = Client{Conn: conn, Name: name}
    mutex.Unlock()

    // Broadcast que le client a rejoint
    broadcastMessage(fmt.Sprintf("%s a rejoint le chat.", name), conn)

    // Envoyer l'historique des messages au nouveau client
    sendHistory(conn)

    for {
        message, err := bufio.NewReader(conn).ReadString('\n')
        if err != nil {
            broadcastMessage(fmt.Sprintf("%s a quitté le chat.", name), conn)
            mutex.Lock()
            delete(clients, conn)
            mutex.Unlock()
            return
        }

        if msg := strings.TrimSpace(message); msg != "" {
            timestamp := time.Now().Format("2006-01-02 15:04:05")
            fullMessage := fmt.Sprintf("[%s][%s]: %s", timestamp, name, msg)
            broadcastMessage(fullMessage, conn)
        }
    }
}

func broadcastMessage(message string, sender net.Conn) {
    mutex.Lock()
    messages = append(messages, message)
    for conn := range clients {
        if conn != sender {
            conn.Write([]byte(message + "\n"))
        }
    }
    mutex.Unlock()
}

func sendHistory(conn net.Conn) {
    mutex.Lock()
    defer mutex.Unlock()
    for _, msg := range messages {
        conn.Write([]byte(msg + "\n"))
    }
}

